# Pergunta pro usuário qual é a base do triângulo
base = input("Qual a base do triângulo? ")

# Pergunta pro usuário qual é a altura do triângulo
altura = input("Qual a altura do triângulo? ")

# Calcula a área do triângulo convertendo as variáveis base e altura para floats
area = (float(base) * float(altura)) / 2

# Printa pro usuário a área do triângulo
print (f"A área do triângulo é {area} unidades!")
